import datetime, time

halloween2019 = datetime.datetime(2019, 10, 31, 0, 0, 0)
newyears2020 = datetime.datetime(2020,1,1,0,0,0)
oct31_2019 = datetime.datetime(2019, 10, 31, 0, 0, 0)
print("DateTime Comparisons")
print('Is Halloween2019 = Oct 31st2019?')
print(halloween2019==oct31_2019)
print('Is Halloween2019 > NewYears2020?')
print(halloween2019>newyears2020)
print('Is NewYears2020 > Halloween2019?')
print(newyears2020>halloween2019)
print('Is NewYears2020 not equal to October31st2019?')
print(newyears2020!=oct31_2019)
print('DateTime Differences')
delta = datetime.timedelta(days=11, hours=10, minutes=9, seconds=8)
print('In 11 days, 10 hrs, 9 mins and 8 secs there are.')
print(str(delta.total_seconds()) + ' seconds.')
dt = datetime.datetime.now()
print('Current System Time')
print(dt)
thousandDays = datetime.timedelta(days=1000)
print('In 1000 Days the Date Time will be.')
print(dt+thousandDays)
thirtyYears = datetime.timedelta(days=365*30)
print('Date about 30 years ago.')
print(dt-thirtyYears)
print('Date about 60 years ago.')
print(dt-(2*thirtyYears))
print('Pausing until this time next year, hit CTRL-C to stop')
nextYear=datetime.timedelta(days=365)
print('Next Year:' + str(dt+nextYear))
try:
    while(datetime.datetime.now()< (dt+nextYear)):
        time.sleep(1)
except KeyboardInterrupt:
    print('\nDone.')